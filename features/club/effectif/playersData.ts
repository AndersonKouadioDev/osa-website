// Exportation de tous les joueurs
export const allPlayers = [...gardiens, ...defenders, ...midfielders, ...forwards];

// Fonction utilitaire pour trouver un joueur par son nom
export const findPlayerByName = (name: string) => {
  return allPlayers.find(player => player.name.toLowerCase() === name.toLowerCase());
};

// Fonction utilitaire pour trouver les joueurs par position
export const findPlayersByPosition = (position: string, excludeName?: string) => {
  return allPlayers.filter(
    player => player.position === position && 
    (!excludeName || player.name.toLowerCase() !== excludeName.toLowerCase())
  );
};
